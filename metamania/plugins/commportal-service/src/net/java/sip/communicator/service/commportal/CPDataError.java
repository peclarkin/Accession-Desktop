package net.java.sip.communicator.service.commportal;

/**
 * enum to cover problems communicating with CommPortal due to the data that we
 * tried to get or send
 */
public enum CPDataError
{
    badRequest,
    expiredToken,
    retryLimitExceeded,
    accountBlocked,
    unknownDataType,
    applicationVersionTooOld,
    serviceUnavailable,
    invalidParameters,
    noSuchObject,
    fileNotFound;
}
